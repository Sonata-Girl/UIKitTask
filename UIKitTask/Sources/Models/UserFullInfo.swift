///  Полная информация о пользователе и его странице
struct UserPageInfo {
    /// Ник
    let name: String
    /// Аватар
    let avatarImage: String
    /// Настоящее имя пользователя
    let realName: String
    /// Количество постов
    let postsCount: Int
    /// Количество подписчиков
    let followersCount: Int
    /// Количество подписок
    let subscriptionsCount: Int
    /// Описание обо мне
    let aboutMe: String
    /// Ссылка
    let link: String
}
