import UIKit

/// Расширение для быстрого доступа к добавленным цветам
extension UIColor {
    var appBeige: UIColor? {
        UIColor(named: "appBeige") ?? nil
    }

    var appBrown: UIColor? {
        UIColor(named: "appBrown") ?? nil
    }

    var appTurquoise: UIColor? {
        UIColor(named: "appTurquoise") ?? nil
    }

    var appLightSky: UIColor? {
        UIColor(named: "appLightSky") ?? nil
    }
}
