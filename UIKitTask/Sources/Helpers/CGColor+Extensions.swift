import UIKit

/// Расширение для быстрого доступа к добавленным цветам
extension CGColor {
    static func appTabBarBorderColor() -> CGColor? {
        UIColor(red: 229 / 255, green: 229 / 255, blue: 229 / 255, alpha: 1).cgColor
    }
}
